/**
 * @param {number[]} nums
 * @return {number[][]}
 */
var permute = function(nums) {
  if (!nums || !nums.length) return []
  const res = []
  const dfs = (path) => {
    if (path.length === nums.length) {
      res.push([...path])
      return
    }

    for (let i = 0; i < nums.length; i++) {
      if (!path.includes(nums[i])) {
        path.push(nums[i])
        dfs(path)
        path.pop()
      }
    }
  }

  dfs([])
  return res
};
