/**
 * @param {string} s
 * @return {string}
 */
var longestPalindrome = function(s) {
  if (s.length < 2) return s
  let start = 0, len = 1
  const n = s.length
  const dp = new Array(n).fill(false).map(() => new Array(n).fill(false))
  for (let i = 0; i < n; i++) {
    dp[i][i] = true
  }
  for (let i = n - 1; i >= 0; i--) {
    for (let j = i + 1; j < n; j++) {
      if (s[i] === s[j]) {
        if (j - i < 3) {
          dp[i][j] = true
        } else {
          dp[i][j] = dp[i + 1][j - 1]
        }
      }

      if (dp[i][j] && j - i + 1 > len) {
        start = i
        len = j - i + 1
      }
    }
  }
  return s.slice(start, start + len)
};

